#pragma mark - Table view data source

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath {
    if (indexPath.section == 0) {
        return 44;
    }
    if (indexPath.section == 1) {
        if (indexPath.row == 0) {
            return 44;
        }else {
            return 88;
        }
    }
    if (indexPath.section == 2) {
        if (indexPath.row == 0) {
            return 44;
        }else {
            return 150;
        }
    }
    return 44;
}


